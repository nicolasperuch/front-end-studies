exports.sum = function (firstValue, secondValue) {
    return firstValue + secondValue;
};

exports.sub = function (firstValue, secondValue) {
    return firstValue - secondValue;
};

exports.multiply = function (firstValue, secondValue) {
    return firstValue * secondValue;
};

exports.division = function (firstValue, secondValue) {
    return firstValue / secondValue;
};